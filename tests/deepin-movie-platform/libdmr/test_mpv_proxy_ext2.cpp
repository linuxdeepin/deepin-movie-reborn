// Extension unit tests for the mpv backend wrapper (suite: mpv_proxy_ext2).
//
// Goal: raise line coverage of src/backends/mpv/mpv_proxy.cpp by exercising the
// STANDALONE, backend-free pieces of the translation unit that the sibling
// suite (mpv_proxy_ext) does NOT already cover. We deliberately avoid anything
// that needs a real MpvProxy instance (its ctor/dtor touch window handles and
// libmpv, which would SIGSEGV/SIGABRT and drop every later case in the run).
//
// Targets (all PURE — no `this`, no mpv_handle):
//   * MpvProxy::tr(...)                 — Q_OBJECT static translator, more strings.
//   * MpvProxy::my_node_autofree        — RAII dtor on DOUBLE / NODE_MAP formats.
//   * MpvHandle                         — copy-assign + reset + operator-mpv_handle.
//   * isSpecialHWHardware()             — free function; with utils::check_wayland_env
//                                         stubbed to false it hits the early
//                                         `return false` (covers the DConfig block +
//                                         wayland guard) without spawning dmidecode.
//
// CRASH SAFETY: every potentially backend-touching member function is SKIPPED,
// not called. mpv_callback() is SKIPPED (it dereferences its void* and drives
// QMetaObject::invokeMethod on the result). No real MpvProxy is constructed.

